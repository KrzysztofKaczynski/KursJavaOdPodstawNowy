package operatory_relacyjne;

public class OperatoryRelacyjne {
    public static void main(String[] args) {

        System.out.println(8 == 3); //false
        System.out.println(8 == 8); //true

        boolean result;
        result = 8 == 8;

        System.out.println("8 == 8: " + result);

        result = 8 == 3;

        System.out.println("8 == 3: " + result);
    }
}
