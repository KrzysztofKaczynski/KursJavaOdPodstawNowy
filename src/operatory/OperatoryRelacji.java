package operatory;

public class OperatoryRelacji {

    public static void main(String[] args) {

        int a = 5;
        int b = 4;

        System.out.println("Wynik dziłania 'a == b': " + (a == b));
        System.out.println("Wynik dziłania 'a == b': " + (a != b));
        System.out.println("Wynik dziłania 'a == b': " + (a > b));
        System.out.println("Wynik dziłania 'a == b': " + (a < b));
        System.out.println("Wynik dziłania 'a == b': " + (a >= b));
        System.out.println("Wynik dziłania 'a == b': " + (a <= b));

    }
}
